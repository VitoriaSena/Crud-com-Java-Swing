    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        
        if(txtbuscarCli.getText().trim().equals("")){
        recarregarTabela();
        }else{
            int numeroBuscar = Integer.parseInt(txtbuscarCli.getText());
           ArrayList<CadasatroCliente>listaRetorno = ClienteDAO.buscarPorNumero(numeroBuscar);
           DefaultTableModel modelo =(DefaultTableModel) tbClientes.getModel();
           for(CadasatroCliente item:listaRetorno){
               modelo.setRowCount(0);
               modelo.addRow(new String[]{
                           String.valueOf(item.getClienteID()),
                        String.valueOf(item.getNome()),
                        String.valueOf(item.getCpf()),
                        String.valueOf(item.getEndereco()),
                        String.valueOf(item.getTelefone())
               });
           }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed
    public void recarregarTabela(){
        
           ArrayList<CadasatroCliente> lista = ClienteDAO.listar();
      
        DefaultTableModel modelo =(DefaultTableModel) tbClientes.getModel();
        modelo.setRowCount(0);
        
        for (CadasatroCliente item : lista) {
            modelo.addRow(new String[]{
                        String.valueOf(item.getClienteID()),
                        String.valueOf(item.getNome()),
                        String.valueOf(item.getCpf()),
                        String.valueOf(item.getEndereco()),
                        String.valueOf(item.getTelefone())                       
            });
                
        }
    }
    private void tglExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tglExcluirActionPerformed
        
        int linhaSelecionada = tbClientes.getSelectedRow();
        
        DefaultTableModel modelo = (DefaultTableModel) tbClientes.getModel();
     
         if (linhaSelecionada >= 0) {
        
        int ExcluirCliente = Integer.parseInt(modelo.getValueAt(linhaSelecionada, 0).toString());
        
        
     boolean retorno = ClienteDAO.Excluir(ExcluirCliente);
        if(retorno){
            JOptionPane.showMessageDialog(rootPane,"Excluido Com Sucesso!");
        }else{
            JOptionPane.showMessageDialog(rootPane, "Falha ao Excluir");
        } 
         
         }else{
            JOptionPane.showMessageDialog(rootPane, "Selecione uma linha!");
        }
        
        
        recarregarTabela();
    }//GEN-LAST:event_tglExcluirActionPerformed

