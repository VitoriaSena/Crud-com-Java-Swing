    private void tgleditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tgleditarActionPerformed
        
     int linhaSelecionada = tbClientes.getSelectedRow();
        
        DefaultTableModel modelo = (DefaultTableModel)tbClientes.getModel();
       
        if (linhaSelecionada >= 0) {
        
        int IdSelecionado = Integer.parseInt(modelo.getValueAt(linhaSelecionada, 0).toString());    
        String nomeSelecionado =  modelo.getValueAt(linhaSelecionada, 1).toString(); 
        String cpfSelecionado =  modelo.getValueAt(linhaSelecionada, 2).toString();  
        String enderecoSelecionado =  modelo.getValueAt(linhaSelecionada, 3).toString(); 
        String telefoneSelecionado =  modelo.getValueAt(linhaSelecionada, 4).toString(); 
        
        CadasatroCliente objAlterar = new CadasatroCliente(IdSelecionado,nomeSelecionado,cpfSelecionado,enderecoSelecionado,telefoneSelecionado);
       
       TelaCadastroClientes alterar = new TelaCadastroClientes(objAlterar);
       alterar.setVisible(true);     
         }else{
            JOptionPane.showMessageDialog(rootPane, "Selecione uma linha!");
        }
        
    }//GEN-LAST:event_tgleditarActionPerformed

