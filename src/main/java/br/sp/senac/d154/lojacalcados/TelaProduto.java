    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

         if(TxtBuscar.getText().trim().equals("")){
        recarregarTabela();
        }else{
            int numeroBuscar = Integer.parseInt(TxtBuscar.getText());
           ArrayList<CadastroProduto>listaRetorno = ProdutoDAO.buscarPorNumero(numeroBuscar);
           DefaultTableModel modelo =(DefaultTableModel) tbProdutos.getModel();
           for(CadastroProduto item:listaRetorno){
               modelo.setRowCount(0);
               modelo.addRow(new String[]{
                           String.valueOf(item.getCodigoP()),
                        String.valueOf(item.getNomeproduto()),
                        String.valueOf(item.getModelo()),
                        String.valueOf(item.getCor()),
                        String.valueOf(item.getTamanho()),
                        String.valueOf(item.getQuantidade()),
                        String.valueOf(item.getPreco()),
               });
           }
        }
                                             
    }//GEN-LAST:event_btnBuscarActionPerformed
public void recarregarTabela(){
        
           ArrayList<CadastroProduto> lista = ProdutoDAO.listar();
      
        DefaultTableModel modelo =(DefaultTableModel) tbProdutos.getModel();
        modelo.setRowCount(0);
        
        for (CadastroProduto item : lista) {
            modelo.addRow(new String[]{
                        String.valueOf(item.getCodigoP()),
                        String.valueOf(item.getNomeproduto()),
                        String.valueOf(item.getModelo()),
                        String.valueOf(item.getCor()),
                        String.valueOf(item.getTamanho()),
                        String.valueOf(item.getQuantidade()),
                        String.valueOf(item.getPreco()),                       
            }); 
                
        }
}
    private void tglbtnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tglbtnRemoverActionPerformed

      int linhaSelecionada = tbProdutos.getSelectedRow();
        
        DefaultTableModel modelo = (DefaultTableModel) tbProdutos.getModel();
        if (linhaSelecionada >= 0) {
     int ExcluirProduto = Integer.parseInt(modelo.getValueAt(linhaSelecionada, 0).toString());
        
        
     boolean retorno = ProdutoDAO.Excluir(ExcluirProduto);
        if(retorno){
            JOptionPane.showMessageDialog(rootPane,"Excluido Com Sucesso!");
        }else{
            JOptionPane.showMessageDialog(rootPane, "Falha ao Excluir");
        }
         }else{
            JOptionPane.showMessageDialog(rootPane, "Selecione uma linha!");
        }
        recarregarTabela();

    }//GEN-LAST:event_tglbtnRemoverActionPerformed

