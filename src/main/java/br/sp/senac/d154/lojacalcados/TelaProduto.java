    private void btnAlterarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarProdutoActionPerformed
         int linhaSelecionada = tbProdutos.getSelectedRow();
        
        DefaultTableModel modelo = (DefaultTableModel)tbProdutos.getModel();
         if (linhaSelecionada >= 0) {
        int IdProdutoSelecionado = Integer.parseInt(modelo.getValueAt(linhaSelecionada, 0).toString());    
        String nomePSelecionado =  modelo.getValueAt(linhaSelecionada, 1).toString(); 
        String ModeloSelecionado =  modelo.getValueAt(linhaSelecionada, 2).toString();  
        String corSelecionada =  modelo.getValueAt(linhaSelecionada, 3).toString(); 
        String tamanhoSelecionado =  modelo.getValueAt(linhaSelecionada, 4).toString(); 
        int quantidadeSelecionada = Integer.parseInt(modelo.getValueAt(linhaSelecionada, 5).toString()); 
        double PrecoSelecionado = Double.parseDouble(modelo.getValueAt(linhaSelecionada, 6).toString()); 
        
        CadastroProduto objAlterar = new CadastroProduto(nomePSelecionado, ModeloSelecionado, tamanhoSelecionado, corSelecionada, quantidadeSelecionada,IdProdutoSelecionado ,PrecoSelecionado);
       
       TelaCadastroProduto alterar = new TelaCadastroProduto(objAlterar);
       alterar.setVisible(true);
         }else{
            JOptionPane.showMessageDialog(rootPane, "Selecione uma linha!");
        }

    }//GEN-LAST:event_btnAlterarProdutoActionPerformed

